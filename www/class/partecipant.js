class Participant {

    constructor(id, name, hp, xp, str, end, wpns, gold) {
        this._id = id;
        this._name = name;
        this._hp = hp;
        this._xp = xp;
        this._str = str;
        this._end = end;
        this._wpns = wpns;
        this._gold = gold;
    }         
}

export default Participant